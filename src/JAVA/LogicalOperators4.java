package JAVA;

public class LogicalOperators4 {

public static void main(String[] args) {
	
int a=30,b=20,c=10;
System.out.println(a<b);
System.out.println(a>b);	
	//&& is used for and locators
	
System.out.println((a>b)&&(b>c));	
//(//)is used for or locators	
System.out.println((a>b)||(b>c));		
	
		
}	
	
	
	
	
	
	
	
	
	
}
